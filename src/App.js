const contractAddress = '0xD27C3F5FdBb985082Cb32F142Ebdd17F3730d795'; // Replace with your contract address

const App = () => {
    const [web3, setWeb3] = useState(null);
    const [account, setAccount] = useState(null);
    const [balance, setBalance] = useState(0);
    const [tokenId, setTokenId] = useState(1); // Default token ID to check
    const [loading, setLoading] = useState(false);

    useEffect(() => {
        if (window.ethereum) {
            const web3Instance = new Web3(window.ethereum);
            setWeb3(web3Instance);
            window.ethereum.request({ method: 'eth_requestAccounts' })
                .then(accounts => {
                    setAccount(accounts[0]);
                })
                .catch(err => console.error('Error fetching accounts:', err));
        } else {
            alert('Please install MetaMask to use this application.');
        }
    }, []);

    const checkTokenBalance = async () => {
        if (!web3) return;
        setLoading(true);
        const contract = new web3.eth.Contract(contractABI, contractAddress);
        try {
            const balance = await contract.methods.balanceOf(account, tokenId).call();
            setBalance(balance);
        } catch (error) {
            console.error('Error checking balance:', error);
            alert('Failed to check token balance');
        }
        setLoading(false);
    };

    const handleTokenIdChange = (event) => {
        setTokenId(event.target.value);
    };

    return (
        <Container className="mt-5">
            <Row className="justify-content-md-center">
                <Col xs={12} md={6}>
                    <h1>Meta Ticket (1st Wave) </h1>
                    {account ? (
                        <div>
                            <Form>
                                <Form.Group className="mb-3" controlId="tokenId">
                                    <Form.Label>Token ID:</Form.Label>
                                    <Form.Control type="number" value={tokenId} onChange={handleTokenIdChange} />
                                </Form.Group>
                                <Button variant="primary" onClick={checkTokenBalance} disabled={loading}>
                                    {loading ? 'Checking...' : 'Check Token Balance'}
                                </Button>
                            </Form>
                            <Alert className="mt-3" variant={balance > 0 ? 'success' : 'warning'}>
                                {balance > 0 ? `Welcome to Meta-Ticket! You have ${balance} tokens of ID ${tokenId}.` : `You do not own any tokens of ID ${tokenId}.`}
                            </Alert>
                        </div>
                    ) : (
                        <Button onClick={() => window.ethereum.request({ method: 'eth_requestAccounts' })}>
                            Connect to MetaMask
                        </Button>
                    )}
                </Col>
            </Row>
        </Container>
    );
};

export default App;
